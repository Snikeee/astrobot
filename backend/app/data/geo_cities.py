def _norm(s: str) -> str:
    return s.strip().lower().replace("ё", "е")


def suggest(query: str, limit: int = 8) -> list[dict]:
    """Подсказать города по началу/вхождению ввода (для автокомплита).

    Возвращает список {"name": город, "country": страна}.
    """
    q = _norm(query)
    if not q:
        return []
    starts = [(n, c) for n, c in SUGGEST if _norm(n).startswith(q)]
    contains = [(n, c) for n, c in SUGGEST if q in _norm(n) and (n, c) not in starts]
    return [{"name": n, "country": c} for n, c in (starts + contains)[:limit]]
